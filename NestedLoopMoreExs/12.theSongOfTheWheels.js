function theSongOfTheWheels(input) {

    let controlNum = Number(input[0]);
    let count = 0;

    let a1 = 0;
    let b1 = 0;
    let c1 = 0;
    let d1 = 0;

    let buff = '';
    let buff1 = '';


    for (let a = 1; a <= 9; a++) {
        for (let b = 1; b <= 9; b++) {
            for (let c = 1; c <= 9; c++) {
                for (let d = 1; d <= 9; d++) {
                    if ((a < b) && (c > d) && controlNum === ((a * b) + (c * d))) {
                        count++;
                        buff += `${a}${b}${c}${d}` + ' ';
                        if (count === 4) {
                            a1 = a;
                            b1 = b;
                            c1 = c;
                            d1 = d;
                            buff1 += `${a1}${b1}${c1}${d1}` + ' ';


                        }
                    }

                }
            }
        }

    }
    if (count >= 4) {
        console.log(buff);
        console.log(`Password: ${buff1} `);
    }
    else {
        console.log(buff);
        console.log('No!');
    }


}

